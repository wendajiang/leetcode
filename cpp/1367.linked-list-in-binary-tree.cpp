/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    bool ans = false;
    bool isSubPath(ListNode* head, TreeNode* root) {
        ans = false;
        if (head == nullptr) 
            return true;
        if (root == nullptr)
            return false;
        travel(root, head);
        return ans;
    }

    void travel(TreeNode* root, ListNode* head) {
        if (root->val == head->val && checkPath(head, root)) {
            ans = true;
            return;
        }
        if (root->left) travel(root->left, head);
        if (root->right) travel(root->right, head);
        
    }

    bool checkPath(ListNode* lnode, TreeNode* tnode) {
        if (lnode == nullptr) 
            return true;
        if (tnode == nullptr) 
            return false;
        if (lnode->val != tnode->val)
            return false;
        return checkPath(lnode->next, tnode->left) || checkPath(lnode->next, tnode->right);
    }
};
